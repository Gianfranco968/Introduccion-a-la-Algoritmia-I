"""Ingresar dos numeros enteros e indicar si son iguales o distintos"""
def comparacion_numeros():
    a = int(input("Ingresar un número: "))
    b = int(input("Ingresar otro número: "))
    if a == b:
        print("Ambos números son iguales")
    else:
        print("Son distintos")
comparacion_numeros()

